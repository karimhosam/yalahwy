    private void submitansActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitansActionPerformed
        if (questionf.getText().equals("")||ans2f.getText().equals("")||ans1f.getText().equals("")||ans3f.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Enter the Question and all answers ", "Can not submit", JOptionPane.ERROR_MESSAGE);
        }
        else {
            Question x= new Question();
            String []anses= new String [3];
            anses[0]=ans1f.getText();
            anses[1]=ans2f.getText();
            anses[2]=ans3f.getText();
            x.question=questionf.getText();
            x.setans(anses);
            counter++;
            newquest.add(x);
            if (counter <getdata.post_details.detalis.size()){
            questionf.setText(getdata.post_details.detalis.get(counter).question);
            ans1f.setText(getdata.post_details.detalis.get(counter).getans(0));
            ans2f.setText(getdata.post_details.detalis.get(counter).getans(1));
            ans3f.setText(getdata.post_details.detalis.get(counter).getans(2));
        }
            else {
                questionf.setText("");
                ans1f.setText("");
                ans2f.setText("");
                ans3f.setText("");
            }
        }
    }//GEN-LAST:event_submitansActionPerformed

