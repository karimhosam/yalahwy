    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (name.getText().equals("")){
            JOptionPane.showMessageDialog(this,"can't leave Empty fields", "Can not submit", JOptionPane.ERROR_MESSAGE);
        }
        else if (newquest.isEmpty()){
            JOptionPane.showMessageDialog(this,"Please Submit Your Questions First", "Can not submit", JOptionPane.ERROR_MESSAGE);
        }
        else {
            SWE.posts.get(state).post_details.setname(name.getText());
            SWE.posts.get(state).post_details.setcategory(cat.getSelectedItem().toString());
            SWE.posts.get(state).post_details.setdate(finddate.getDate());
            SWE.posts.get(state).post_details.setphoto(photodic.getText());
            SWE.posts.get(state).post_details.detalis.clear();
            for (int i=0;i<newquest.size();i++)
                SWE.posts.get(state).post_details.detalis.add(newquest.get(i));
            try {
                JOptionPane.showMessageDialog(this,"Post Modified Successfully", "Done !", JOptionPane.PLAIN_MESSAGE);
                getdata.setdata();
                IModify next = new IModify ();
                next.show();
                this.dispose();
            } catch (IOException ex) {
                Logger.getLogger(ToModify.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

